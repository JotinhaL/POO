    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Endereco tempEndereco = new Endereco(txtEndLog.getText(),Integer.parseInt(txtEndNum.getText()),txtEndBai.getText(),
                txtEndCdd.getText(),txtEndUF.getText(),txtEndCEP.getText());
        if(this.flag==1){
            Date tempDt = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            try {
                tempDt = sdf.parse(txtPessDt.getText());
            } catch (ParseException ex) {
                tempDt = null;
            }
            Cliente tempCliente = new Cliente(txtPessCPF.getText() ,txtPessNome.getText(),
                    tempEndereco,tempDt,txtEstCivil.getText(),txtPessEsc.getText(),null);
            tempCliente.setTelefone(txtPessTel.getText());
            if(btnH.isSelected()){
                tempCliente.setSexo('H');
            }else tempCliente.setSexo('M');
            this.dtCl.cadastrar(tempCliente);
        }else if(this.flag==2){
            Funcionario f;
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date tempDt;
            try {
                tempDt = sdf.parse(txtPessDt.getText());
            } catch (ParseException ex) {
                tempDt = null;
            }
            if(btnFuncGer.isSelected()){
                f = new Gerente(txtPessNome.getText(),tempEndereco,txtPessCPF.getText(),txtFuncCTPS.getText(),new Date(),null,false);
                f.setCargo("Gerente");
                f.setEstCivil(txtEstCivil.getText());
                f.setEscolaridade(txtPessEsc.getText());
                f.setTelefone(txtPessTel.getText());
                f.setDataNasc(tempDt);
            }
            else{
                f = new Funcionario(txtPessCPF.getText(),txtPessNome.getText(),tempEndereco,txtEstCivil.getText(),txtPessEsc.getText(),
                        txtPessTel.getText(), tempDt,'\0',txtFuncCTPS.getText(),new Date(),txtFuncCargo.getText());
            }
            if(btnH.isSelected()){
                f.setSexo('H');
            }else f.setSexo('M');
            try {
                tempDt = sdf.parse(txtDtAdm.getText());
            } catch (ParseException ex) {
                tempDt = null;
            }
            f.setDataAdm(tempDt);
            this.dtFc.cadastrar(f);
        }else if(this.flag == 3){
            Gerente gr = (Gerente)this.dtFc.buscar(txtAgcGer.getText());
            Agencia tempAgencia = new Agencia(txtAgcNome.getText(),Integer.parseInt(txtAgcNum.getText()),tempEndereco,gr);
            this.dtAg.cadastrar(tempAgencia);
            gr.setAgencia(tempAgencia);
        }else if(this.flag==12){
            Funcionario tf = dtFc.buscar(txtRemove.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            tf.setCPF(txtPessCPF.getText());
            tf.setCTPS(txtFuncCTPS.getText());
            tf.setCargo(txtFuncCargo.getText());
            try {
                tf.setDataNasc(sdf.parse(txtPessDt.getText()));
            } catch (ParseException ex) {}
            tf.setEndereco(tempEndereco);
            tf.setEscolaridade(txtPessEsc.getText());
            tf.setEstCivil(txtEstCivil.getText());
            tf.setNome(txtPessNome.getText());
            tf.setTelefone(txtPessTel.getText());
            if(btnH.isSelected()){
                tf.setSexo('H');
            }else tf.setSexo('M');
        }else if(this.flag==11){
            Cliente tc = dtCl.buscar(txtRemove.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            tc.setCPF(txtPessCPF.getText());
            try {
                tc.setDataNasc(sdf.parse(txtPessDt.getText()));
            } catch (ParseException ex) {}
            tc.setEndereco(tempEndereco);
            tc.setEscolaridade(txtPessEsc.getText());
            tc.setEstCivil(txtEstCivil.getText());
            tc.setNome(txtPessNome.getText());
            if(btnH.isSelected()){
                tc.setSexo('H');
            }else tc.setSexo('M');
            tc.setTelefone(txtPessTel.getText());
        }else if(this.flag==13){
            Agencia ta = dtAg.buscar(Integer.parseInt(txtRemove.getText()));
            ta.setEndereco(tempEndereco);
            ta.setGerente((Gerente)dtFc.buscar(txtAgcGer.getText()));
            ta.setNome(txtAgcNome.getText());
            ta.setNumero(Integer.parseInt(txtAgcNum.getText()));
        }
        setIn();
        limpar();
    }//GEN-LAST:event_jButton6ActionPerformed

