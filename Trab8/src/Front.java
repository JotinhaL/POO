    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(this.flag==10){
            Conta tc = dtCt.buscar(Integer.parseInt(txtRemove.getText()));
            tc.setNumero(Integer.parseInt(txtContNum.getText()));
            tc.setSenha(passConta.getText());
            tc.setAgencia(dtAg.buscar(Integer.parseInt(txtContaAg.getText())));
            return;
        }
        Conta tempConta;
        if(btnTCorr.isSelected()){
            tempConta = new ContaCorrente();
        }else if(btnTPou.isSelected()){
            tempConta = new ContaPoupanca();
        }
        else{
            tempConta = new ContaSalario();
        }
        tempConta.setNumero(Integer.parseInt(txtContNum.getText()));
        tempConta.setSenha(passConta.getText());
        if(bntAtv.isSelected()){
            tempConta.setAtivo('A');
        }else tempConta.setAtivo('I');
        Cliente tempCl = dtCl.buscar(txtContaTitu.getText());
        Agencia tempAg = dtAg.buscar(Integer.parseInt(txtContaAg.getText()));
        tempCl.setAgencia(tempAg);
        tempConta.setAgencia(tempAg);
        tempConta.addTitular(tempCl);
        dtCt.cadastrar(tempConta);
        setIn();
        limpar();
    }//GEN-LAST:event_jButton4ActionPerformed

