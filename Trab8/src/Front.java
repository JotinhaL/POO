    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        if(this.flag==1){
            Conta tempC = dtCt.buscar(Integer.parseInt(txtCtO.getText()));
            if(passCtO.getText().equals(tempC.getSenha())){
                tempC.sacar(Float.parseFloat(txtValor.getText()));
            }
        }else if(this.flag==2){
            Conta tempC = dtCt.buscar(Integer.parseInt(txtCtD.getText()));
            tempC.depositar(Float.parseFloat(txtValor.getText()));
        }else{
            Conta tempC = dtCt.buscar(Integer.parseInt(txtCtO.getText()));
            if(passCtO.getText().equals(tempC.getSenha())){
                Conta tempD = dtCt.buscar(Integer.parseInt(txtCtD.getText()));
                tempC.transferencia(Float.parseFloat(txtValor.getText()), tempD);
            }
        }
        setIn();
        limpar();
    }//GEN-LAST:event_jButton12ActionPerformed

